"""Synthetic interview simulator for offline evaluation.

The real system uses a closed-source LLM judge whose calls cost money and
have non-deterministic latency. To run controlled ablations we model both
the candidate and the LLM judge analytically:

    Candidate model (per category c):
        latent_ability θ_c ~ Beta(α_c, β_c)
        answer_quality on a question of difficulty d :
            q_t = clip( σ(k * (θ_c - d)) + noise, 0, 1 )

    Multi-judge LLM model (paper §4.1):
        for j = 1..J:
            judge_jt = clip( q_t + bias_j + ε_jt, 0, 1 )
        where ε_jt has heavier tails for low-quality answers (the place where
        real LLM judges actually disagree most). The ensemble's trimmed mean
        is what the experiment harness compares against true q_t.

    Question selection (paper §4.2):
        bandit policy chooses a category given memory features; the chosen
        category determines which θ_c drives the next answer.

Everything below is pure Python + numpy and is independent of the FastAPI
runtime so the experiment runner can sweep thousands of episodes quickly.
"""

