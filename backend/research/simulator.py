RUBRIC_KEYS = REQUIRED_DIMENSIONS
RUBRIC_WEIGHTS = {
    "relevance": 0.18,
    "knowledge": 0.20,
    "specificity": 0.18,
    "reasoning": 0.16,
    "completeness": 0.14,
    "reflection": 0.08,
    "follow_up": 0.06,
}


